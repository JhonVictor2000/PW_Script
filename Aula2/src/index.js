function calculaNumeros(n1, n2, op){
    if (op == "+"){
        return (n1 + n2);
    }if (op == "-"){
        return (n1 - n2);
    }
    if (op == "raiz"){
        return (n1, n2);
        Math.sqrt
    }
    if (op == "potencia"){
        return (n1, n2)
        Math.sinh
    }
}

console.log(calculaNumeros(100, 10, "raiz"));